    <h2 class="text-3xl font-bold text-center text-gray-800 mb-6">¿Qué es Cinetria?</h2>
    <p class="text-gray-700 text-lg leading-relaxed max-w-3xl mx-auto text-center">
        Cinetria es un proyecto final de grado de <strong>Desarrollo de Aplicaciones Web</strong> hecho durante el curso 2024/2025.
        <br>
        La idea principal es crear una aplicación web que permita a los usuarios buscar y descubrir películas, series y actores de manera sencilla y rápida.
        También pueden crear reseñas y valoraciones, lo que les permite compartir sus opiniones con la comunidad.
        <br>
        Utilizando la API de <strong><a href="https://www.themoviedb.org/?language=es" target="blank">The Movie Database (TMDb)</a></strong>, los usuarios pueden explorar una amplia base de datos de contenido audiovisual, ver detalles sobre cada título y encontrar información sobre sus actores favoritos.
    </p>
